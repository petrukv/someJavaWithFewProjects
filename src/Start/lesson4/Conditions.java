package Start.lesson4;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;

        if (x > y) {
            System.out.println("x > y");
        } else if (x == y) {
            System.out.println(" X = Y");
        } else {
            System.out.println("Y > X");
        }
    }
}
